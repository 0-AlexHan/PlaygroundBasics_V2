func checkIs(superset: Set<Int>, of: [Set<Int>]) -> Bool {
    for each in of {
        if !each.isSubset(of: superset) {
            return false
        }
    }
    return true
}

let givenSetOfNums: [Set<Int>] = [[1, 2], [2, 3], [1, 3], [1, 2, 3]]
let superset: Set<Int> = [1, 2, 3]

print(checkIs(superset: superset, of: givenSetOfNums))


